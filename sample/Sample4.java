package sample;

public class Sample4 {
	public static void main(String[] args) {
		/* 得点(70点)を変数に格納 */
		int tensuu = 70;

		if (tensuu >= 90) {
			System.out.println("大変よくできました");
		} else if (tensuu >= 70) {
			System.out.println("よくできました");
		} else {
			System.out.println("次回頑張りましょう");
		}
	}
}