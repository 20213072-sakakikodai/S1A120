package practice1;

public class P1_02_Calc {
	public int add (int n1, int n2) {
		int answer = 0;
		answer = n1+n2;
		return answer;
	}
	
	public static int sub (int n1, int n2) {
		int answer = 0;
		answer = n1-n2;
		return answer;
	}
	
	public static int mlt (int n1, int n2) {
		int answer = 0;
		answer = n1*n2;
		return answer;
	}
	
	public static int div (int n1, int n2) {
		int answer = 0;
		answer = n1/n2;
		return answer;
	}
}
